package stringclass;

public class StringClassExample {
    public static void main(String[] args) {
        String a ="hello";
        String b ="no hello";

        System.out.println(a);
        System.out.println(b);
        System.out.println();
        System.out.println();
        System.out.println();

        a="hi";
        b="no hi";

        System.out.println(a);
        System.out.println(b);

    }
}
